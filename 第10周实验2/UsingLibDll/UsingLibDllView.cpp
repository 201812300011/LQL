// CUsingLibDllView 绘制

void CUsingLibDllView::OnDraw(CDC* /*pDC*/)
{
	CUsingLibDllDoc* pDoc = GetDocument();
	ASSERT_VALID(pDoc);
	if (!pDoc)
		return;

	// TODO: 在此处为本机数据添加绘制代码
	CString s;
	int n = 5;
	int a = factorial(n);
	if (a>0)
		s.Format(_T("%d的阶乘为：%d"), n, a);
	else
		s.Format(_T("负数不可以进行阶乘运算"));
	GetDC()->TextOutW(50, 100, s);


	CString ss;
	float edg = 30.0;
	FAC a1;
	float ans = a1.convert(edg);
	ss.Format(_T("convert(30.0) 的值:%f"), ans);
	GetDC()->TextOutW(100, 150, ss);
}


